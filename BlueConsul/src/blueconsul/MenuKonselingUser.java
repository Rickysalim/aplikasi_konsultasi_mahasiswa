    private void showListKonsulType() { 
        try {
            DefaultListModel model = new DefaultListModel();
            konsul_type_list.setModel(model);
            int row = konsul_type_list.getModel().getSize();
            for (int i = 0; i < row; i++) {
               model.removeElement(0);
            }
            Connection cn = new Connection();
            ResultSet rs = cn.Select("select * from konsul_type");
            while(rs.next()) {
                model.addElement(rs.getString(2));
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    };
    
    private void showListKonsulTermAndCondition(String value) {
        try {
            Connection cn = new Connection();
            ResultSet rs = cn.Select("select konsul_term_and_condition.term_and_condition from konsul_term_and_condition, konsul_type where konsul_term_and_condition.id_konsul_type = konsul_type.id and konsul_type.konsul_type='"+value+"'");
            while(rs.next()) {
                term_and_condition_textfield.setText(rs.getString(1));
                term_and_condition_textfield.setEditable(false);
            }  
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    

