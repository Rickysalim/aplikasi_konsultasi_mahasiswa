    private void NotSatisfiedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NotSatisfiedButtonActionPerformed
        // TODO add your handling code here:
          try {
            Connection cn = new Connection();
            String selected = konsul_type_list.getSelectedValue();
            System.out.println(selected);
            if (selected == null) {
                JOptionPane.showMessageDialog(null, "Pilih Jenis Konsul Terlebih Dahulu");
                return;
            }
            ResultSet rs = cn.Select("select konsul_type.id from konsul_type where konsul_type.konsul_type='"+selected+"'");
            while(rs.next()) {
             ResultSet rs2 = cn.Select("select * from konsul_satisfied where id_konsul_type="+rs.getString(1)+" and is_satisfied='no'");
              if (rs2.next()) {
                  this.dispose();
                  new QuestionBoxUser(username_label.getText(), getRoleId(), getUserId()).setVisible(true);
              } else {
                  cn.Insert("insert into konsul_satisfied(users_id,id_konsul_type,is_satisfied) values('" + getUserId() + "'," +rs.getString(1)+ ",'"+"no"+"')");
                  this.dispose();
                  new QuestionBoxUser(username_label.getText(), getRoleId(), getUserId()).setVisible(true);
              }   
            } 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_NotSatisfiedButtonActionPerformed

    private void SatisfiedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SatisfiedButtonActionPerformed
        // TODO add your handling code here:
           try {
            Connection cn = new Connection();
            String selected = konsul_type_list.getSelectedValue();
            if (selected == null) {
                JOptionPane.showMessageDialog(null, "Pilih Jenis Konsul Terlebih Dahulu");
                return;
            }
            ResultSet rs = cn.Select("select konsul_type.id from konsul_type where konsul_type.konsul_type='"+selected+"'");
            while(rs.next()) {
             ResultSet rs2 = cn.Select("select * from konsul_satisfied where id_konsul_type="+rs.getString(1)+" and is_satisfied='yes'");
              if (rs2.next()) {
                  this.dispose();
                  new DashboardUser(username_label.getText(), getRoleId(), getUserId()).setVisible(true);
              } else {
                  cn.Insert("insert into konsul_satisfied(users_id,id_konsul_type,is_satisfied) values('" + getUserId() + "'," +rs.getString(1)+ ",'"+"yes"+"')");
                  this.dispose();
                  new DashboardUser(username_label.getText(), getRoleId(), getUserId()).setVisible(true);
              }   
            } 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SatisfiedButtonActionPerformed

