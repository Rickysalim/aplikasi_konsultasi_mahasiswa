    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Connection cn = new Connection();
        ResultSet rs = cn.Select("select id from users where users.user_name='"+combo_box.getSelectedItem()+"'");
        try {
            while(rs.next()) {
                cn.Insert("insert into konsul_mhs(users_id, midterm_exam, final_exam) values(" + rs.getString(1) + "," + uts_value.getText() + "," + uas_value.getText() + ")");
                JOptionPane.showMessageDialog(null, "berhasil memasukan data");
                showMhsUser();   
                reset();
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuMonitorAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        Connection cn = new Connection();
        ResultSet rs = cn.Select("select id from users where users.user_name='"+combo_box.getSSelectedItem()+"'");
        try {
            while(rs.next()) {
                cn.Update("update konsul_mhs set midterm_exam="+uts_value.getText()+", final_exam="+uas_value.getText()+" where users_id="+rs.getString(1)+"");
                JOptionPane.showMessageDialog(null, "berhasil perbaharui data");
                showMhsUser();   
                reset();
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuMonitorAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UpdateActionPerformed

