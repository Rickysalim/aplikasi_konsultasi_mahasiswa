    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        try {
            Connection cn = new Connection();
            ResultSet rs = cn.Select("select id from users where users.user_name='" + combo_box.getSelectedItem() + "'");
            while(rs.next()) {
                ResultSet rs2 = cn.Select("select id from konsul_mhs where konsul_mhs.users_id=" + rs.getString(1));
                while(rs2.next()) {
                    cn.Delete("delete from konsul_mhs where id="+rs2.getString(1));
                    JOptionPane.showMessageDialog(null, "berhasil perbaharui data");
                    showMhsUser();
                    reset();
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuMonitorAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_DeleteActionPerformed

    private void reset() {
         combo_box.setSelectedIndex(0);
         combo_box.enable();
         uts_value.setText("");
         uas_value.setText("");
    }
    
    public void showListUser() {  
        try {
            Connection cn = new Connection();
            ResultSet rs2 = cn.Select("select * from users");
            while(rs2.next()) {
                model2.addElement(rs2.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MenuMonitorAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void showMhsUser() {
        try {
           int row = table_data.getRowCount();
           for (int i = 0; i < row; i++) {
               model.removeRow(0);
           }
            Connection cn = new Connection();
            ResultSet rs = cn.Select("select users.user_name, konsul_mhs.midterm_exam, konsul_mhs.final_exam  from konsul_mhs, users where users.id = konsul_mhs.users_id;");
            
            while(rs.next()) {
               String data [] = {rs.getString(1),rs.getString(2),rs.getString(3)};
               model.addRow(data);
            }
         
        } catch (SQLException ex) {
            Logger.getLogger(MenuMonitorAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
   
    
